#include "main.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * _abs - this function computes the absolute value of an integer
 * main - entry point, starts the program
 * @n: the int to checked
 * Return: the absolute value of int n
 */
int _abs(int n)
{
	if (n >= 0)
	{
		return (n);
		return (-n);
	}
	return 0;
}
